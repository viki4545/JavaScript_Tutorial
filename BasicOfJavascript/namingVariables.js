// rules for naming variables

// you cannot start with number
// example :-
// 1value (invalid)
// value1 (valid)

    var value1 = 16;

    // single astreik(*) for multiplication
    console.log(value1 * 2);
    // double astreik(**) for finding out the power
    console.log(value1 ** 2);

// you can use only underscore _ or dollar symbol
// first_name (valid)
// _firstname (valid)

var _firstname = "vicky";
console.log(_firstname);

// first$name (valid)
// $firstname (valid)

var $firstname = "mohit";

//  you cannot use spaces
    // first name (invalid)
    // var first name = "harshit";

var first_name = "harshit"; // snake case writing
var lastName = "Gupta"; // camel case writing

// convention
// start variables name with small letter and use camelCase
var lastName = "Gupta"; // camel case writing